package main.java.typeOfBoat;

public class TypeBoat {
    private String typ;

    public TypeBoat(String typ) {
        super();
        this.typ = typ;
    }

    public String getTyp() {
        return typ;
    }

    public void setTyp(String typ) {
        this.typ = typ;
    }

    public String toString() {
        return this.typ;
    }
}


